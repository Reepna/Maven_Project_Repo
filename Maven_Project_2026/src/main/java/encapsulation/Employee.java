package encapsulation;

public class Employee {

	private double salary; 
	private String name;
	
	public void setSalary(double salary) {
		
	    this.salary = salary;
	   
	}
	
	public double getSalary() {
		
		return salary;
		
	}
	
	public void setName(String name) {
		
	    this.name = name;
	   
	}
	
	public String getName() {
		
		return name;
		
	}

	
}
